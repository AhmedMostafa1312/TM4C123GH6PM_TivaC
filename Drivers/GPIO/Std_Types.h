#ifndef STD_TYPES_H_
#define STD_TYPES_H_

/*-----for unsigned types-----*/
typedef unsigned char  uint8;
typedef unsigned short uint16;
typedef unsigned long  uint32;

/*-----for signed types-----*/
typedef signed char  int8;
typedef signed short int16;
typedef signed long  int32;

/*-----for floar and double-----*/
typedef float  f32;
typedef double f64;

#endif /* STD_TYPES_H_ */
